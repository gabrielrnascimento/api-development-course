app = FastAPI()

origins = ["*"]

app.add_middleware(
    CORSMiddleware,
    allow_origins=origins,
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)

# ! routing magic !!!!
app.include_router(post.router)
app.include_router(user.router)
app.include_router(auth.router)
